# Important ML Libraries & Project Context

# 1. NumPy: Numerical Python. Used for working with arrays, matrices, and intense mathematical operations linearly.
# 2. Pandas: Data analysis and manipulation tool. Provides DataFrames (tabular structures) which are essential handling datasets (CSV, JSON, SQL).
# 3. Matplotlib: Comprehensive library for creating static, animated, and interactive visualizations (plots).
# 4. Seaborn: Data visualization library based on matplotlib. Provides a high-level interface for drawing attractive statistical graphics (like Heatmaps, Pairplots).
# 5. Scikit-Learn: Base library for Machine Learning. Contains all statistical modeling algorithms (regression, classification, clustering) and preprocessing tools.
# 6. TensorFlow: Open source framework by Google for machine learning. Particularly strong in deep learning and neural networks.
# 7. PyTorch: Developed by Facebook. Highly preferred in research for deep learning due to dynamic computational graphing.
# 8. Keras: High-level neural networks API, running on top of TensorFlow to allow rapid prototyping.
# 9. XGBoost: Optimized distributed gradient boosting library. Fast, scalable and handles missing data inherently.

